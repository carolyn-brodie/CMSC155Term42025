package week1.basics;

public class TryFloatingPt {
    public static void main(String[] args) {
        int x = 5, y = 2;
        // Multiple variables can be declared together.
        double z = 5;	  //double is a floating point number.
        // floating point number stored in "double precision"
        System.out.println("x = " + x + " x/y = " + x/y );
        System.out.println("x/y as a float = " + (float) x/y );
        System.out.println("z = " + z + " z/y = " + z/y );

    }
}
